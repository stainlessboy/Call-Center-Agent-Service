RAW_DB_URL = os.getenv("DATABASE_URL", "sqlite:///./bot.db")


def _normalize_db_url(url: str) -> str:
    if url.startswith("sqlite+aiosqlite://"):
        parsed = urlparse(url.replace("sqlite+aiosqlite://", "sqlite://", 1))
        return urlunparse(parsed)
    return url


